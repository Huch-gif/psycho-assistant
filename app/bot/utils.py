# app/bot/utils.py

def is_crisis_message(text: str) -> bool:
    """
    Проверяет, содержит ли сообщение признаки психологического кризиса.
    Возвращает True, если обнаружены ключевые слова.
    """
    crisis_keywords = [
        "убить", "умереть", "самоубийство", "покончить с собой",
        "нет смысла жить", "я не выдержу", "я хочу исчезнуть",
        "суицид", "самоповреждение", "помогите", "спасите",
        "больше не могу", "всё кончено", "никому не нужен",
        "хочу умереть", "жизнь не имеет смысла", "ненавижу себя",
        "лучше бы меня не было", "не вижу выхода"
    ]
    text_lower = text.lower()
    return any(keyword in text_lower for keyword in crisis_keywords)